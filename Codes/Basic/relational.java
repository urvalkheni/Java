
public class relational {
       public static void main(String[] args) {
           int x = 5;
           int y= 6;
           System.out.println(x == y);
           System.out.println(x != y);
           System.out.println(x > y);
           System.out.println(x < y);
           System.out.println(x >= y);
           System.out.println(x <= y);


           double a  = 5.5;
           double b = 7.7;
           System.out.println(a == b);
           System.out.println(a != b);
           System.out.println(a > b);
           System.out.println(a < b);
           System.out.println(a >= b);
           System.out.println(a <= b);

       }    
}
